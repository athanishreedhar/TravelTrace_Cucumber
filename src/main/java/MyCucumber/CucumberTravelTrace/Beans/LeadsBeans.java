package MyCucumber.CucumberTravelTrace.Beans;

public class LeadsBeans {
	
	private static String FirsName;
	private static String Lastname;
	private static String Mobile;
	private static String Wmail;
	private static String Company;
	
	public static String getFirsName() {
		return FirsName;
	}
	public static void setFirsName(String firsName) {
		FirsName = firsName;
	}
	public static String getLastname() {
		return Lastname;
	}
	public static void setLastname(String lastname) {
		Lastname = lastname;
	}
	public static String getMobile() {
		return Mobile;
	}
	public static void setMobile(String mobile) {
		Mobile = mobile;
	}
	public static String getWmail() {
		return Wmail;
	}
	public static void setWmail(String wmail) {
		Wmail = wmail;
	}
	public static String getCompany() {
		return Company;
	}
	public static void setCompany(String company) {
		Company = company;
	}
	
	
}
